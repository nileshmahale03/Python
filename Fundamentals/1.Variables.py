#Variable Types
age = 25              # int
temperature = 98.6    # float
is_true = True        # bool
name = "Alice"        # str
var = None            # NoneType

print(type(age), type(temperature), type(is_true), type(name), type(var))

#Variables are dynamically typed
n = 0 
print(n)

n = "abc"
print(n)

#Multiple assignments
a, b, c = 0.125, "abc", False

#Type casting
variable = 10.9
print(int(variable))

a = 5
print(str(a))
print(bool(a))
print(float(a))